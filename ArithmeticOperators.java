// Arithmetic Operators

public class ArithmeticOperators {
    public static void main(String[] args) {
        int num1 = 15, num2 = 8;
        System.out.println("Addition : value of num1 + num2 is " + (num1 + num2));
        System.out.println("Subtraction : value of num1 - num2 is " + (num1 - num2));
        System.out.println("Multiplication : value of num1 * num2 is " + (num1 * num2));
        System.out.println("Division : value of num1 / num2 is " + (num1 / num2));
        System.out.println("ModuloDivision : value of num1 % num2 is " + (num1 % num2));
    }
}

